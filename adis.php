			<h3>Akses Kawasan Gunung Gede Pangrango</h3>
			<p>Ada 6 pintu wisata menuju kawasan TNGGP yaitu: Cibodas, Gunung Putri, Bodogol, Cisarua, Selabintana dan Situgunung.

Kantor Balai TNGGP, pusat informasi (visitor center) dan tempat pendaftaran pendakian berlokasi di Cibodas. <p>
	<img src="petaakses.jpg"width="500" height="480">
			<p>Pintu masuk Cibodas, Gunung Putri dan Selabintana merupakan akses utama menuju puncak Gunung Gede dan Pangrango. Pintu masuk Situgunung merupakan pintu menuju Danau Situgunung yang sangat sesuai untuk rekreasi keluarga. Sedangkan, Pusat Pendidikan Konservasi Alam Bodogol dengan jembatan kanopi sepanjang 400 m memiliki daya tarik bagi pengunjung dan masyarakat umum yang ingin berekreasi dengan merasakan keindahan hutan hujan tropis. Cisarua juga pintu masuk yang dekat dari Jakarta, mempunyai fasilitas untuk kemping yang cocok bagi keluarga, anak sekolah dan kelompok-kelompok pecinta alam.</p>
			<p>Pintu Masuk Dengan Mobil Pribadi Dengan Transportasi Umum</p>
			<p>cibodas</p>
			<p>Berjarak 100 km dari Jakarta. Dapat ditempuh melalui Jalan Tol Jagorawi dan keluar di Tol Ciawi. Di pertigaan Ciawi, ambil jurusan Puncak – Bandung. Setelah 7,6 km dari Puncak Pass Hotel, setelah Outlet DSE, belok ke kanan tepat pada pertigaan di Paragajen (Papan Nama Taman Nasional Gunung Gede Pangrango ada disebelah kiri jalan). Jalan lurus kira-kira 3 km dan sampai pada portal pintu Gerbang Wisata Cibodas, dan disini ada restribusi (mobil dan kendaraan roda dua Rp.3000,- dan setiap penumpang Rp. 1000,-/orang). Tidak jauh dari portal ini, anda menemukan kantor Taman Nasional Gunung Gede Pangrango disebelah kanan. Dengan bis umum dari Bogor – Bandung, Jakarta – Bandung yang lewat Puncak. Turun di Pertigaan di Paragajen, dekat Outlet DSE. (Pertigaan ini disebut Pertigaan Cibodas, dan papan nama TNGP disebelah kiri jalan). Dari pertigaan, anda naik angkot warna kuning (Cibodas, Rarahan) dengan ongkos Rp. 2000 per orang sampai di pintu gerbang TNGP. Tarif ojek sampai ke pintu gerbang kantor TNGP Rp. 6000,-.
Guung Putri Terletak 15 km dari Cibodas. Pengunjung dapat menuju lokasi ini dari Cipanas dengan jarak kira-kira 7 km. Lokasi Kemping Bobojong di Gunung Putri berjarak 1 km jalan kaki dari terminal angkot di Gunung Putri. Pengunjung harus naik angkot dari terminal Cipanas ke Gunung Putri dengan ongkos Rp. 3000,- /orang.</p>
			<p>Selabintana Berjarak 10 km atau 30 menit dari Sukabumi, melewati jalan perkebunan teh dan kebun sayur. Pintu masuk Selabintana yaitu di Pondok Halimun berada di Cipelang. Dari terminal bis Sukabumi dengan minibus menuju pusat kota dan kemudian ganti kendaraan dengan minibus yang menuju Pondok Halimun.</p>
			<p>Situgunung</p>
			<p>Pintu masuk Situgunung terletak kira-kira 70 km atau 1.5 jam dari Bogor. Dari Bogor, ambil jurusan Sukabumi dan kemudian berbelok di Cisaat menuju Situgunung. Situgunung terletak di sebelah selatan kawasan Taman Nasional. Akses cukup bagus. Dari Jakarta atau Bogor, ambil bis jurusan Jakarta – Sukabumi – Cisaat. Jika dari terminal Sukabumi, naik minibus yang menuju Cisaat, dan sampai di Cisaat, ambil minibus menuju Situgunung, yang berjarak 10 km.</p>
			<p>Bodogol </p>
			<p>Dari Bogor ke pintu masuk Bodogol, ambil jurusan Sukabumi dan turun di Lido (kira-kira 25km). Dari Lido menuju desa Bodogol kira-kira 4km, dan dari desa Bodogol menuju PPKAB kira-kira 3 km melalui jalan berbatu, dan disarankan menggunakan kendaraan roda 4 dengan gardan ganda. Dengan menggunakan bis atau mini bus dari Bogor dengan ongkos Rp. 5,000-/orang. Dari Lido anda dapat menggunakan motor ojek menuju resort Bodogol dengan ongkos Rp. 5,000-/orang. Dari resort Bodogol, anda dapat mengunakan ojek sampai PPKAB.</p>
			<p>Cisarua </p>
			<p>Pintu gerbang Cisarua berjarak kira-kira 14 km atau 20 menit dari Ciawi dengan mobil. Menuju pintu gerbang akses cukup bagus dengan jalan aspal. Dari Ciawi, gunakan minibus menuju terminal Pasir Muncang, dan dari terminal ini sewa ojek menuju pintu masuk Cisarua.</p>
