			<h3>Sejarah dan Legenda TNGP</h3>
<p>Gunung Gede Pangrango ditetapkan sebagai salah satu dari 5 taman nasional pertama di Indonesia oleh pemerintah Indonesia melalui Surat Keputusan Menteri Pertanian tahun 1980.<p>

<p>sejarah awal konservasi di kawasan ini hanya sedikit diketahui, walaupun hutan dan gunung merupakan bagian dari legenda-legenda di tanah Sunda. Tampaknya ada jalur sejarah dari kota tua Cianjur sampai Bogor melalui Cipanas. Bagian lereng pegunungan yang rendah, tidak rata dan berteras-teras dulunya digunakan untuk pertanian dengan pergiliran tanaman.<p>

<p>Dikenalkannya tanaman teh sebagai tanaman perkebunan memberikan dampak nyata bagi kawasan ini. Teh varietas Jepang telah ditanam sejak tahun 1728, dan perkebunan ini terbentang mulai dari Ciawi sampai Cikopo di tahun 1835. Kemudian, tahun 1878, teh Assam diperkenalkan dan tumbuh dengan sangat baik, menyebabkan ekonomi dan kondisi lingkungan di kampung-kampung dilereng pegunungan berubah.<p>

<p>Sejarah panjang kegiatan konservasi dan penelitian dimulai sejak tahun 1830 dengan terbentuknya kebun raya kecil di dekat Istana Gubernur Jenderal Kolonial Belanda di Cipanas, dan kemudian kebun raya kecil ini diperluas sehingga menjadi Kebun Raya Cibodas sekarang ini. Pemerintahan Kolonial Belanda sangat antusias untuk meningkatkan tanaman-tanaman penting dan bernilai ekonomis serta perkebunan komersial, sehingga dibanguna suatu stasiun penelitian dan percobaan pertanian di dataran tinggi ini. Tidak lama setelah itu, botanis-botanis lokal kemudian mulai tertarik untuk meneliti keanekaragaman tumbuhan disekitar pegunungan ini. Abad 19 merupakan masa-masa terbesar dan penting dalam sejarah koleksi tumbuhan , dan Cibodas menjadi salah satu lokal koleksi tumbuhan saat itu.<p>

<p>Tahun 1889, areal hutan antara Kebun Raya Cibodas dan Air Panas ditetapkan sebagai Cagar Alam. Setelah tahun 1919, suatu kawasan cagar alam ditetapkan. Komitmen utama dimulai tahun 1978, ketika kawasan seluas 14,000 hektar, yang terdiri dari 2 puncak utama dan lerengnya yang luas, ditetapkan sebagai Cagar Biosfer Gunung Gede Pangrango. Akhirnya, tahun 1980, seluruh kawasan terpisah-pisah ini digabung menjadi Taman Nasional Gunung Gede Pangrango.<p>

<p>Legenda dan Kepercayaan<p>

<p>The “holly man” in Cikundul fall at CibeureumPencarian sampai bagian dari kawasan Gunung Gede dan Pangrango yang terdalam, anda tidak akan terkejut untuk menemukan bahwa kawasan ini kaya dengan sejarah dan legenda. Cerita-cerita tersebut menjadi kunci kepada kekaguman kita terhadap gunung ini.<p>

<p>Di Cibeureum, ada suatu batu besar di air terjun Cikundul. Menurut legenda setempat, tempat formasi batu tersebut berada dahulu merupakan tempat dimana seorang yang dipercayai sangat sakti sedang bersila dan melakukan meditasi, saking lamanya bersila dan meditasi, akhirnya orang sakti tersebut berubah menjadi batu. Pada hari kiamat, dipercayai bahwa dia akan berubah wujud menjadi manusia kembali. Dalam cerita ini, kejadian alam dan spritual tidak dapat dipisahkan.</p>
